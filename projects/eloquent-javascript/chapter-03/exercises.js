function countChars(string, char) {
  //create an array to push matches into
  var array = [];
    //iterate through string
    for (var i = 0; i <= string.length - 1; i++){
      //write a conditional statement
      if (string[i] === char){
        //push into array
        array.push(string[i]);
      }
    }
        //return array length
        return array.length;

}

